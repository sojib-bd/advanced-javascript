// double equal compare the value as true or false

let firstNumb = 1;
let secondNumb = "1";

firstNumb == secondNumb ? console.log("condition is true") : console.log("condition is false");

// triple equal compare the value as well as the type 

let one = 1;
let two = "1";

one === two ? console.log("condition is true") : console.log('condition is false')


